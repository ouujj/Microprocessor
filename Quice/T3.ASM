	ORG	0000H
	
	MOV	R0,#80H ;DPH START
	MOV	R1,#00H	;DPL START
	
	MOV	R2,#80H	;DPH FINAL
	MOV	R3,#10H ;DPL FINAL
	
	
LOOP:	;READ
	MOV	DPH,R0
	MOV	DPL,R1 
	MOVX	A,@DPTR
	INC	DPTR 
	MOV	R0,DPH
	MOV	R1,DPL
	;WRITE
	MOV	DPH,R2
	MOV	DPL,R3 
	MOVX	@DPTR,A
	INC	DPTR 
	MOV	R2,DPH
	MOV	R3,DPL
	
	CJNE	R1,#10H,LOOP
	
		
	
	
	SJMP	$
	END